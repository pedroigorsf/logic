
controle = int(input("Digite um número inteiro:  ") )
print("-----------------------------")
numero = controle
resultado=1

while controle != 0:
    resultado = controle
    print("\nResultado: ",resultado)
    print("Fator: ",resultado*numero)
    controle = controle - 1

print("\nWhile acabou de fazer todos os números fatoriais")
    


